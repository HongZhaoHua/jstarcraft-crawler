package com.jstarcraft.crawler.trade.security.stock;

public enum Measure {

    /** 价格 */
    PRICE,

    /** 市净率(Price to Book Ratio) */
    PB,

    /** 市盈率(Price to Earning Ratio) */
    PE,

    /** 市销率(Price to Sale Ratio) */
    PS,

    /** 资产回报率(Return on Asset Ratio) */
    ROA,

    /** 权益回报率(Return on Equity Ratio) */
    ROE,

    /** 股息率(Dividend Yield Ratio) */
    DY,

    /** 派息率(Dividend Payout Ratio) */
    DP;

}
